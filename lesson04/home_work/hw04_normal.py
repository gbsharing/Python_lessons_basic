# Задание-3:
# Напишите скрипт, заполняющий указанный файл (самостоятельно задайте имя файла)
# произвольными целыми цифрами, в результате в файле должно быть
# 2500-значное произвольное число.
# Найдите и выведите самую длинную последовательность одинаковых цифр
# в вышезаполненном файле.

import os, random

number = ''
for i in range(2500):
    number += str(random.randint(0, 9))

# Записываем данные в файл
with open(os.path.join("2500-number.txt"), "w", encoding="UTF-8") as f:
    f.write(number)

arr = re.findall(r'((\d)\2+)', number)
numbers = list(map(lambda x: x[0], arr))
max_length = max(list(map(lambda x: len(x), numbers)))
max_numbers = list(filter(lambda x: len(x) == max_length, numbers))
print(max_numbers)
